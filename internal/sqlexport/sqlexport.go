// hasModifier reports whether col has a modifier of the given kind.
func (g *generator) hasModifier(col *ast.Column, kind ast.ModifierKind) bool {
	for _, mod := range col.Modifiers {
		if mod.Kind == kind {
			return true
		}
	}
	return false
}
