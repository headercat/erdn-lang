func TestGenerateSQLite(t *testing.T) {
	out := generateSQL(t, blogSchema, DBMSSQLite)

	// Identifier quoting
	if !strings.Contains(out, `"authors"`) {
		t.Error("SQLite: expected double-quoted table name")
	}
	// INTEGER PRIMARY KEY AUTOINCREMENT inline
	if !strings.Contains(out, `"id" INTEGER PRIMARY KEY AUTOINCREMENT`) {
		t.Error("SQLite: expected inline INTEGER PRIMARY KEY AUTOINCREMENT")
	}
	// No separate ALTER TABLE FK (SQLite FK is inline)
	if strings.Contains(out, "ALTER TABLE") {
		t.Error("SQLite: unexpected ALTER TABLE (SQLite uses inline FK)")
	}
	// Inline FOREIGN KEY constraint
	if !strings.Contains(out, `FOREIGN KEY ("author_id") REFERENCES "authors" ("id")`) {
		t.Error("SQLite: expected inline FOREIGN KEY constraint")
	}
	// PRAGMA foreign_keys
	if !strings.Contains(out, "PRAGMA foreign_keys = ON") {
		t.Error("SQLite: expected PRAGMA foreign_keys = ON")
	}
	// NOW() → CURRENT_TIMESTAMP
	if !strings.Contains(out, "DEFAULT CURRENT_TIMESTAMP") {
		t.Error("SQLite: expected DEFAULT CURRENT_TIMESTAMP")
	}
}

