func TestGenerateTypeMapping(t *testing.T) {
	src := `table types (
  a bool
  b boolean
  c text
  d double
  e blob
  f json
  g uuid
)`

	cases := []struct {
		dbms DBMS
		want []string
	}{
		{DBMSMySQL, []string{"TINYINT(1)", "TEXT", "DOUBLE", "BLOB", "JSON", "CHAR(36)"}},
		{DBMSPostgreSQL, []string{"BOOLEAN", "TEXT", "DOUBLE PRECISION", "BYTEA", "JSON", "UUID"}},
		{DBMSMSSQL, []string{"BIT", "NVARCHAR(MAX)", "FLOAT", "VARBINARY(MAX)", "NVARCHAR(MAX)", "UNIQUEIDENTIFIER"}},
		{DBMSOracle, []string{"NUMBER(1)", "CLOB", "FLOAT(126)", "BLOB", "CLOB", "CHAR(36)"}},
		{DBMSSQLite, []string{"INTEGER", "TEXT", "REAL", "BLOB", "TEXT", "TEXT"}},
	}

	for _, tc := range cases {
		out := generateSQL(t, src, tc.dbms)
		for _, want := range tc.want {
			if !strings.Contains(out, want) {
				t.Errorf("DBMS %s: expected %q in output:\n%s", tc.dbms, want, out)
			}
		}
	}
}

func TestGenerateSelfReferentialLink(t *testing.T) {
	src := `
table categories (
  id        bigint primary-key auto-increment
  parent_id bigint nullable
  name      varchar(128) not-null
)
link one categories.id to many categories.parent_id
`
	// MySQL: self-referential FK via ALTER TABLE
	out := generateSQL(t, src, DBMSMySQL)
	if !strings.Contains(out, "ALTER TABLE `categories` ADD CONSTRAINT `fk_categories_parent_id` FOREIGN KEY (`parent_id`) REFERENCES `categories` (`id`)") {
		t.Error("MySQL: expected self-referential ALTER TABLE FK")
	}

	// SQLite: self-referential FK inline
	out = generateSQL(t, src, DBMSSQLite)
	if !strings.Contains(out, `FOREIGN KEY ("parent_id") REFERENCES "categories" ("id")`) {
		t.Error("SQLite: expected inline self-referential FOREIGN KEY")
	}
}
