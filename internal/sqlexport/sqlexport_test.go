func generateSQL(t *testing.T, src string, dbms DBMS) string {
	t.Helper()
	prog, err := parser.ParseString(src)
	if err != nil {
		t.Fatalf("parse error: %v", err)
	}
	errs := semantic.Validate(prog)
	if len(errs) > 0 {
		t.Fatalf("validation errors: %v", errs)
	}
	return Generate(prog, dbms)
}

const blogSchema = `
table authors (
  id       bigint      primary-key auto-increment
  username varchar(64) not-null indexed
  email    varchar(255) not-null indexed
  bio      text        nullable
)

table posts (
  id        bigint       primary-key auto-increment
  author_id bigint       not-null indexed
  title     varchar(512) not-null
  status    varchar(32)  not-null default("draft")
  created_at timestamp   not-null default(NOW())
)

link one authors.id to many posts.author_id
`

func TestValidDBMS(t *testing.T) {
	for _, name := range SupportedDBMS {
		if !ValidDBMS(name) {
			t.Errorf("ValidDBMS(%q) = false, want true", name)
		}
	}
	if ValidDBMS("unknown") {
		t.Error("ValidDBMS(\"unknown\") = true, want false")
	}
}

func TestGenerateHeader(t *testing.T) {
	for _, dbms := range []DBMS{DBMSMySQL, DBMSPostgreSQL, DBMSMSSQL, DBMSOracle, DBMSSQLite} {
		out := generateSQL(t, `table t (id bigint primary-key)`, dbms)
		if !strings.Contains(out, "-- Generated by erdn-lang") {
			t.Errorf("DBMS %s: missing header comment", dbms)
		}
		if !strings.Contains(out, string(dbms)) {
			t.Errorf("DBMS %s: missing DBMS name in header", dbms)
		}
	}
}

func TestGenerateMySQL(t *testing.T) {
	out := generateSQL(t, blogSchema, DBMSMySQL)

	// Identifier quoting
	if !strings.Contains(out, "`authors`") {
		t.Error("MySQL: expected backtick-quoted table name `authors`")
	}
	// AUTO_INCREMENT
	if !strings.Contains(out, "AUTO_INCREMENT") {
		t.Error("MySQL: expected AUTO_INCREMENT")
	}
	// Type mappings
	if !strings.Contains(out, "VARCHAR(64)") {
		t.Error("MySQL: expected VARCHAR(64)")
	}
	if !strings.Contains(out, "TIMESTAMP") {
		t.Error("MySQL: expected TIMESTAMP")
	}
	// Default string conversion
	if !strings.Contains(out, "DEFAULT 'draft'") {
		t.Error("MySQL: expected DEFAULT 'draft'")
	}
	// NOW() preserved
	if !strings.Contains(out, "DEFAULT NOW()") {
		t.Error("MySQL: expected DEFAULT NOW()")
	}
	// Primary key constraint
	if !strings.Contains(out, "PRIMARY KEY (`id`)") {
		t.Error("MySQL: expected PRIMARY KEY (`id`)")
	}
	// Foreign key
	if !strings.Contains(out, "ALTER TABLE `posts` ADD CONSTRAINT `fk_posts_author_id` FOREIGN KEY (`author_id`) REFERENCES `authors` (`id`)") {
		t.Error("MySQL: expected ALTER TABLE foreign key statement")
	}
	// Index
	if !strings.Contains(out, "CREATE INDEX `idx_authors_username` ON `authors` (`username`)") {
		t.Error("MySQL: expected CREATE INDEX for indexed column")
	}
}

